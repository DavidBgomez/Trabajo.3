    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        String error;
        double valorA, valorB, valorC, resultado1, resultado2;
        
        try{
            valorA=Double.parseDouble(txtValorA.getText());
            valorB=Double.parseDouble(txtValorB.getText());
            valorC=Double.parseDouble(txtValorC.getText());
            resultado1=Ecuacion.calcularResultado1EcuacionSegundoGrado(valorA, valorB, valorC);
            resultado2=Ecuacion.calcularResultado2EcuacionSegundoGrado(valorA, valorB, valorC);
            
            if ((resultado1==-1)&&(resultado2==-1)){
                error=("Esta ecuacion no tiene solucion");
                txtResultado1.setText(String.valueOf(error));
                txtResultado2.setText(String.valueOf(error));
                
            } else{
                txtResultado1.setText(String.valueOf(resultado1));
                txtResultado2.setText(String.valueOf(resultado2));
            }
            
        } catch(NumberFormatException nfe){
            error =("Error: Dato no numerico");
            txtResultado1.setText(String.valueOf(error));
            txtResultado2.setText(String.valueOf(error));
            
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

