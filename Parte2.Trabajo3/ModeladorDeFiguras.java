    private void btnCalcularTrianguloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularTrianguloActionPerformed
        // TODO add your handling code here:
        int baseTriangulo, alturaTriangulo;
        double areaTriangulo, perimetroTriangulo, hipotenusa;
        String error, tipoTriangulo;
        
        try{
            baseTriangulo = Integer.parseInt(txtBaseTriangulo.getText());
            alturaTriangulo = Integer.parseInt(txtAlturaTriangulo.getText());
            areaTriangulo = TrianguloRectangulo.calcularAreaTrianguloRectangulo(baseTriangulo, alturaTriangulo);
            perimetroTriangulo = TrianguloRectangulo.calcularPerimetroTrianguloRectangulo(baseTriangulo, alturaTriangulo);
            hipotenusa = TrianguloRectangulo.calcularHipotenusa(baseTriangulo, alturaTriangulo);
            tipoTriangulo = TrianguloRectangulo.determinarTipoDeTriangulo(baseTriangulo, alturaTriangulo);
            txtAreaTriangulo.setText(String.valueOf(areaTriangulo));
            txtPerimetroTriangulo.setText(String.valueOf(perimetroTriangulo));
            txtHipotenusa.setText(String.valueOf(hipotenusa));
            txtTipoTriangulo.setText(String.valueOf(tipoTriangulo));
        }
        catch(NumberFormatException nfe){
            error="Dato no numerico";
            txtAreaTriangulo.setText(error);
            txtPerimetroTriangulo.setText(error);
            txtHipotenusa.setText(error);
            txtTipoTriangulo.setText(error);
        }               
        
    }//GEN-LAST:event_btnCalcularTrianguloActionPerformed

