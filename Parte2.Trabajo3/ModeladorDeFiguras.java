    private void btnCalcularCirculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularCirculoActionPerformed
        // TODO add your handling code here:
        int radioCirculo;
        double areaCirculo, perimetroCirculo;
        String error;
        try{
            radioCirculo = Integer.parseInt(txtRadioCirculo.getText());
            areaCirculo = Circulo.calcularAreaCirculo(radioCirculo);
            perimetroCirculo = Circulo.calcularPerimetroCirculo(radioCirculo);
            txtAreaCirculo.setText(String.valueOf(areaCirculo));
            txtPetrimetroCirculo.setText(String.valueOf(perimetroCirculo));
        }
        catch(NumberFormatException nfe){
            error="Dato no numerico";
            txtAreaCirculo.setText(error);
            txtPetrimetroCirculo.setText(error);
        }
    }//GEN-LAST:event_btnCalcularCirculoActionPerformed

